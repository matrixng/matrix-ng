module core_clk#(
      parameter PRESCALER = 16
)(
      input clk,
      input rst,

      output reg [PRESCALER-1:0] core_clk);

initial begin
core_clk = 0;
end

always @(posedge clk or posedge rst ) begin
  if (rst) 
    core_clk <= 0;
  else 
    core_clk <= core_clk + 1;
end

endmodule
